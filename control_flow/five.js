/*Grade System  
Input marks and print grade using if-else if:

A: ≥90

B: ≥75

C: ≥50

F: <50*/
const marks = 10;
 if (marks >=90){
    console.log("Grade - A")
 }
 else if (marks >=75){
    console.log("Grade - B")
 }
 else if (marks >=50){
    console.log("Grade - C")
 }
 else{
    console.log("Grade - F ❌")
 }